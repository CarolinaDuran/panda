          <p class="intro ">
            Uno de los problemas a los cuales se enfrentan las familias de hijos/as con TDAH es el fracaso escolar de éstos. Este problema, que tiende
             a desesperar a los padres, se deriva de las dificultades que a veces surgen a la hora de organizarles el estudio.
          </p>
          <img class="izq" src="images/causas/factores.jpg">
          <p class="intro cons">
            Un niño/a con TDAH necesita rutinas diarias, que le marquen sus pautas de actuación
             en cada momento, y estas rutinas son necesarias tanto en casa como en el colegio.
          </p>

          <p class="intro cons">
            Los niños/as con TDAH, a veces se enfrentan a problemas de incomprensión en el entorno, que en ocasiones no termina de entender la conducta de estos niños/as, caracterizada en gran medida por la prevalencia
             de despistes, olvidos, problemas de concentración, desorganización, impulsividad, dificultad para planificar el tiempo, etc.
          </p>
          <p class="intro cons">
            Todo esto, hace imprescindible la necesidad de establecer rutinas, que de manera progresiva, les faciliten la consecución de sus logros y objetivos.
          </p>
          <p class="intro cons">
            Para ello, resulta de vital importancia planificar las tareas de estos /as niños/as de manera secuencial y estructurada, facilitándoles el conocimiento de qué hacer en cada momento.
            Para ello deben tenerse en cuenta una serie de recomendaciones:
          </p>
          <ul class="intro2">
            <li class="cons">Diseñarles un horario detallado con todas las tareas y actividades que deberán hacer durante el día. Para ello, puede ser de gran utilidad emplear colores, esquemas, imágenes  y en definitiva, elementos atractivos, que de manera visual le ayuden a distinguir rápidamente qué deben hacer en cada momento.</li>
            <li class="cons">Destinar un lugar tranquilo a sus tareas, alejado de elementos distractores, donde pueda realizar sus actividades dedicándoles la mayor atención posible.</li>
            <li class="cons">Introducir actividades amenas y entretenidas después de aquellas de obligada realización (como por ejemplo, tareas escolares); de esta manera aumentaremos la motivación del niño/a.</li>
            <li class="cons">Planificar los cambios, es decir, si en algún momento se tiene que modificar el horario del niño/a por alguna razón (un festejo, un acontecimiento social diferente, etc); se le debe preparar con antelación, ya que  los cambios les causan ansiedad y frustración. De manera que para evitar estas reacciones es aconsejable prepararle previamente.</li>
            <li class="cons">Involucrarles en tareas extraescolares deportivas y culturales (música, teatro, deportes…) que les diviertan y al mismo tiempo contribuyan a mejorar su motivación para aprender.</li>
            <li class="cons">A lo largo del día, se debe dedicar tiempo para analizar con el niño/a los logros conseguidos y preguntarle por las dificultades u obstáculos encontrados, con el fin de trabajarlos. Y sobre todo, se debe emplear el refuerzo positivo ante tareas que haya realizado adecuadamente, pues esto contribuirá a  mantener un nivel de motivación constante. En este sentido, el empleo de técnicas como la economía de fichas, puede contribuir a reforzar la confianza en sí mismo y en consecuencia su responsabilidad y su compromiso en la realización de sus siguientes quehaceres.</li>
            <li class="cons">Con la finalidad de mejorar su autoestima, es aconsejable también, realizar actividades en equipo con otros / as niños y niñas del grupo de iguales, que les hagan sentir integrad@s en éste y contribuyan así, a un aumento de su autoestima, pues en ocasiones, los niños y niñas con TDAH suelen presentar dificultades a la hora de hacer amig@s, hecho éste que repercute seriamente en ella.</li>
            <li class="cons">Ayudarles a descubrir sus virtudes y recordárselas, es también una buena estrategia para motivarles y conseguir que confíen en ell@s mism@s y sus posibilidades.</li>
            <li class="cons">Asimismo, debemos resaltar la importancia de dejarles tiempo suficiente para la realización completa de sus tareas, evitando prisas o presiones que les puedan agobiar. Poco a poco, conseguirán realizarlas y mejorar su aprendizaje.</li>
          </ul>
