          TDAH son las siglas de Trastorno por Déficit de Atención e Hiperactividad.
          Se trata de un trastorno de carácter neurobiológico originado en la infancia que implica un patrón de déficit de atención, hiperactividad y/o impulsividad, y que en muchas ocasiones está asociado con otros trastornos comórbidos.
          Es fundamental para el diagnóstico de TDAH evaluar que estos síntomas nucleares que hemos comentado
          (déficit de atención, hiperactividad e impulsividad) se presenten:
          </p>
            <img class="der" src="images/TDA/que.jpg">
          <ol class="intro2">
            <li>Desde una edad temprana: antes de los 12 años </li>
            <li>con una intensidad y frecuencia superior a la normal para la edad y la etapa de desarrollo del niño. </li>
            <li>Que deterioren o interfieran de forma significativa en el rendimiento del niño en dos o más de los ámbitos
              de su vida: escolar o laboral, familiar y social.</li>
            <li>No ser causados por otro problema médico, un tóxico, una droga u otro problema psiquiátrico </li>
          </ol>
          <p class="intro">
          Pese a que pueda existir sospecha clínica en niños de menos de 6 años el diagnóstico de TDAH requiere haber superado esta edad. Además, es frecuente que el TDAH se reconozca en los niños
          cuando comienza la educación primaria, coincidiendo con dificultades en el rendimiento escolar y la presentación de disfunciones sociales.
          </p>
