          <h2 align="center">¿Quiénes son más propensos a padecer TDAH, los niños o las niñas?</h2>
        </div>
        <p class="intro">
          En la población pediátrica, se identifica el TDAH más en varones que en mujeres. La mayoría de los expertos creen que
          se comunica una incidencia mucho menor de TDAH en niñas y mujeres adolescentes porque las niñas tienden a sufrir el
          subtipo con predominio de inatención y es el subtipo hiperactivo (y la perturbación asociada) el que llama la atención
          de los adultos y precipita la derivación del paciente para ser valuado. Por tanto, la relación de varones a niñas con
          un diagnóstico de TDAH es de más o menos 4:1 en la población general.
